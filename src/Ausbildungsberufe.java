public class Ausbildungsberufe {
    private int id;
    private String bezeichnung;
    private String volleBezeichnung;

    public Ausbildungsberufe(int id, String bezeichnung, String volleBezeichnung) {
        this.id = id;
        this.bezeichnung = bezeichnung;
        this.volleBezeichnung = volleBezeichnung;
    }
}
